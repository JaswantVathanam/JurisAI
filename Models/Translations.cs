    /// <summary>
    /// Get translated text for a key in the specified language
    /// Falls back to English if translation not found
    /// </summary>
    public static string Get(string key, string languageCode = "en")
    {
        if (TranslationDictionary.TryGetValue(key, out var translations))
        {
            if (translations.TryGetValue(languageCode, out var translation))
            {
                return translation;
            }
            // Fallback to English
            if (translations.TryGetValue("en", out var englishTranslation))
            {
                return englishTranslation;
            }
        }
        // Return key as fallback
        return key;
    }

    /// <summary>
    /// Check if a translation exists for a key
    /// </summary>
    public static bool HasTranslation(string key, string languageCode = "en")
    {
        return TranslationDictionary.TryGetValue(key, out var translations) 
               && translations.ContainsKey(languageCode);
    }

